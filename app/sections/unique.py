with open("uniques.csv") as csv_file:
    text = csv_file.read()
    lines = text.split("\n")[1:-1]
    uniques = []
    for line in lines:
        parts = line.split(",")
        name = parts[0].strip('"')
        base_type = parts[6].strip('"')
        uniques.append(
            {
                "name": name,
                "base_type": base_type,
            }
        )

unique_base_types = list(set([unique["base_type"] for unique in uniques]))

dust_values_for_bases = []
for unique_base_type in unique_base_types:
    possible_uniques = [
        item["name"] for item in uniques if item["base_type"] == unique_base_type
    ]
    multipliers = [
        float(item["multiplier"])
        for item in dust_uniques
        if item["name"] in possible_uniques
    ]
    dust_values_for_bases.append(
        {
            "base_type": unique_base_type,
            "multipliers": multipliers or [0],
        }
    )

good_dust_uniques = [
    item["base_type"]
    for item in dust_values_for_bases
    if min(item["multipliers"]) >= 10
]

